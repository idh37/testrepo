// NMContego internal Error code
//
//  Error Combine
//	- 0x00 00 0000 : kernel error(2byte) + uer error(2byte) + window last error(4byte)
//
#define NMCTG_ERROR_INVALID_PARAM			0x00010000
#define NMCTG_ERROR_APIHOOK					0x00020000
#define NMCTG_ERROR_APIUNHOOK				0x00030000
#define NMCTG_ERROR_APIHOOKDLL_LOAD			0x00040000
#define NMCTG_ERROR_NOT_SURPPORT			0x00050000
//#define NMCTG_ERROR_PROCESSIMG_INFO			0x00060000	-> not used
#define NMCTG_ERROR_ALREADY_LOADED			0x00070000

#define NMCTG_ERROR_KBFLTDRV_LOAD			0x00080000
#define NMCTG_ERROR_KBFLTDRV_IOCTRL			0x00090000
#define NMCTG_ERROR_KB_SETWINHOOK			0x000A0000

#define NMCTG_ERROR_MOFLTDRV_LOAD			0x000B0000
#define NMCTG_ERROR_MOFLTDRV_IOCTRL			0x000C0000
#define NMCTG_ERROR_MO_SETWINHOOK			0x000D0000

#define NMCTG_ERROR_HKDRV_LOAD				0x000E0000
#define NMCTG_ERROR_HKDRV_IOCTRL			0x000F0000
#define NMCTG_ERROR_HKDRV_SCRCPT			0x00100000
#define NMCTG_ERROR_HKDRV_PROMEM			0x00110000
#define NMCTG_ERROR_HKDRV_HIDEPROC			0x00120000
#define NMCTG_ERROR_HKDRV_PROINPUT			0x00130000


